from scrapy import Field, Item

class CraigslistItem(Item):
    # define all item fields
    title = Field()
    price = Field()
    priceUnit = Field()
    cityName = Field()
    description = Field()
    imageUrls = Field()
    locAccuracy = Field()
    lat = Field()
    lng = Field()
    postId = Field()
    postingUrl = Field()
    postDate = Field()
    updateDate = Field()
    attributes = Field()
    sellerUrl = Field()
    sellerName = Field()
    sellerPhoneNumber = Field()
    sellerEmail = Field()
    scrapeDate = Field()
    specs = Field()
    processingStatus = Field()
